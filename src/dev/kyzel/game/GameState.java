package dev.kyzel.game;

/**
 * All possible game states.
 */
public enum GameState {
    
    /**
     * The title state.
     */
    TITLE,

    /**
     * The pausing state.
     */
    PAUSE,

    /**
     * The playing state.
     */
    PLAYING,

    /**
     * The game-over state.
     */
    OVER
}