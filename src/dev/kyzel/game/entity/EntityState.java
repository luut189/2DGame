package dev.kyzel.game.entity;

/**
 * All the possible states of an entity.
 */
public enum EntityState {

    /**
     * Entity is walking.
     */
    WALKING,

    /**
     * Entity is swimming.
     */
    SWIMMING,

    /**
     * Entity is attacking something.
     */
    ATTACKING,

    /**
     * Entity is standing still.
     */
    STANDING
}
